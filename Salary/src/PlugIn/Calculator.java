    private double value1,value2;
    private char operator=' ';
    private boolean clickOperator=false;
    private void calculator(char op){
        value1=Double.valueOf(txtNumber.getText());
        operator=op;
        clickOperator=true;
    }
    private void cmdEqualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdEqualActionPerformed
        value2=Double.valueOf(txtNumber.getText());
        double r=0;
        if(operator=='+')
            r=value1+value2;
        else if(operator=='-')
            r=value1-value2;
        
        txtNumber.setText(""+r);
        operator=' ';
        // TODO add your handling code here:
    }//GEN-LAST:event_cmdEqualActionPerformed

