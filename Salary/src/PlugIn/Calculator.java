    private void clickButton(JButton cmd){
        if(clickOperator==true)
            txtNumber.setText("");
        String num=txtNumber.getText();
        if(num.length()<12){
            num+=cmd.getText();
            txtNumber.setText(num);
        }else{
            Toolkit.getDefaultToolkit().beep();
        }
        clickOperator=false;
    }
